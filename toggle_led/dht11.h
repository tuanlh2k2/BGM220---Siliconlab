#include <stdio.h>
#include <stdint.h>

#ifndef DHT11_H
#define DHT11_H

/***************************************************************************//**
 * Khởi tạo chân dữ liệu và bắt đầu quá trình liên kết với dht11.
 ******************************************************************************/
void dht_init(void);

/***************************************************************************//**
 * Gửi xung start cho cảm biến để bắt đầu quá trình đọc dữ liệu.
 ******************************************************************************/
void dht_start(void);

/***************************************************************************//**
 * Nhận phản hồi từ cảm biến.
 ******************************************************************************/
void dht_response(void);

/***************************************************************************//**
 * Nhận 8 bit dữ liệu.
 ******************************************************************************/
uint8_t dht_get_data();

#endif  // DHT11_H
